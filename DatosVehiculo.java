package gestionvehiculos;

public class DatosVehiculo {
    String placa;
    String color;
    String linea;
    String modelo;
    String propietario;

    public DatosVehiculo(String placa, String color, String linea, String modelo, String propietario) {
        this.placa = placa;
        this.color = color;
        this.linea = linea;
        this.modelo = modelo;
        this.propietario = propietario;
    }
}